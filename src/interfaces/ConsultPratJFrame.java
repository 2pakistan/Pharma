    private void listPraticiensActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listPraticiensActionPerformed

        //ON ATTEND QUE LA LISTE SOIT REMPLIE !!
        if (listPraticiens.getItemCount() < 1) {

        } else {
            String id = listPraticiens.getSelectedItem().toString();
            int idEl = Integer.parseInt(id);
            try {

                PraticienDAO praticien1 = new PraticienDAO();
                Praticien praticienFound = praticien1.getPraticienById(idEl);

                inputNom.setText(praticienFound.getNom());
                inputPrenom.setText(praticienFound.getPrenom());
                inputAdresse.setText(praticienFound.getAdresse());
                inputVille.setText(praticienFound.getVille());
                inputCoef.setText(""+praticienFound.getCoefNotoriete());
                inputStatut.setText(""+praticienFound.getStatut());
                inputCp.setText(""+praticienFound.getCodePostal());

            } catch (SQLException e1) {
                e1.printStackTrace();
            }
        }

        //on instancie un objet EleveDao et on utilise la methode getEleveById() qui retourne un objet eleve

    }//GEN-LAST:event_listPraticiensActionPerformed


    private void btnValidFormActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidFormActionPerformed
        // TODO add your handling code here:
        
        String nom         = inputNom.getText().substring(0, 1).toUpperCase() + inputNom.getText().substring(1);
        String prenom      = inputPrenom.getText();
        String ville       = inputVille.getText().toUpperCase();
        String adresse     = inputAdresse.getText();
        int cp             = Integer.parseInt(inputCp.getText());
        String libelleType = comboTypePrat.getSelectedItem().toString();
        int statut = 0 ;
        double coef = 0;

        //On recupere la checkbox coch√©e 
        if(radioBtnTitulaire.isSelected()){
            statut = 1;
        }else if(radioBtnRemplacant.isSelected()){
            statut = 2;
        }
        
        // On recupere le code associ√© au libelle selectionn√© (type praticien ) 
        TypePratDAO typeDao = new TypePratDAO();
        String typeCode = typeDao.findCodeByLibelleType(libelleType);
        
        //On hydrate un objet Praticien
        Praticien pratToAdd = new Praticien(nom, prenom, adresse, cp, ville, coef, statut, typeCode);
        
        //On utilise la classe DAO praticien pour inserer en BDD
        PraticienDAO pratDao = new PraticienDAO();
        pratDao.insertPraticien(pratToAdd);
        
         JOptionPane msgInserted = new JOptionPane();
         msgInserted.showMessageDialog(null, "Le praticien a bien √©t√© ajout√© ", "Information", JOptionPane.INFORMATION_MESSAGE);
         
         //On vide les champs apres insertion
        inputNom.setText("");
        inputPrenom.setText("");
        inputVille.setText("");
        inputAdresse.setText("");
        inputCp.setText("");
        comboTypePrat.setSelectedItem(0);
        radioBtnTitulaire.setSelected(false);
        radioBtnRemplacant.setSelected(false);
    }//GEN-LAST:event_btnValidFormActionPerformed

